//arithmatic operators
#include <iostream>
using namespace std;

int main() {
    int a = 5;
    int b = 3;

    cout << "Addition: " << a + b << endl; // 8
    cout << "Subtraction: " << a - b << endl; // 2
    cout << "Multiplication: " << a * b << endl; // 15
    cout << "Division: " << a / b << endl; // 1
    cout << "Modulus: " << a % b << endl; // 3
    cout << "Exponentiation: " << pow(a, b) << endl; // 125

    return 0;
}